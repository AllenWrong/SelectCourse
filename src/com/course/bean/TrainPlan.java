package com.course.bean;

public class TrainPlan {
	private String majorPlanName;
	private String majorPlanGrade;
	private String tpCoureNum;
	private String couName;
	private String couType;
	private int couRedit;
	private String cou_xs;
	private String tpTerm;
	
	
	public String getMajorPlanName() {
		return majorPlanName;
	}
	public void setMajorPlanName(String majorPlanName) {
		this.majorPlanName = majorPlanName;
	}
	public String getMajorPlanGrade() {
		return majorPlanGrade;
	}
	public void setMajorPlanGrade(String majorPlanGrade) {
		this.majorPlanGrade = majorPlanGrade;
	}
	public String getTpCoureNum() {
		return tpCoureNum;
	}
	public void setTpCoureNum(String tpCoureNum) {
		this.tpCoureNum = tpCoureNum;
	}
	public String getCouName() {
		return couName;
	}
	public void setCouName(String couName) {
		this.couName = couName;
	}
	public String getCouType() {
		return couType;
	}
	public void setCouType(String couType) {
		this.couType = couType;
	}
	public int getCouRedit() {
		return couRedit;
	}
	public void setCouRedit(int couRedit) {
		this.couRedit = couRedit;
	}
	public String getCou_xs() {
		return cou_xs;
	}
	public void setCou_xs(String cou_xs) {
		this.cou_xs = cou_xs;
	}
	public String getTpTerm() {
		return tpTerm;
	}
	public void setTpTerm(String tpTerm) {
		this.tpTerm = tpTerm;
	}
	
}
